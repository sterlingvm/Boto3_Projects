#Import Dependencies & Libraries
import pandas as pd
from flask import Flask, render_template

#-----------------------------------------------------------------------------------------------------------------------------------------------
#-----------------------------------------------------------------------------------------------------------------------------------------------
#Create Flask App

app = Flask(__name__)

#-----------------------------------------------------------------------------------------------------------------------------------------------
#-----------------------------------------------------------------------------------------------------------------------------------------------
#Define Data & Create DataFrame

df = pd.DataFrame({
    'ID':[1,2,3,4,5,6,7,8,9],
    'Name':["Jason","Todd","Sarah","Jessica","Tyler","Dick","Alex","Santi","Gabriella"],
    'Group':[1,2,1,2,2,2,1,1,1]
})

#-----------------------------------------------------------------------------------------------------------------------------------------------
#-----------------------------------------------------------------------------------------------------------------------------------------------
#Validate the DataFrame

df

#-----------------------------------------------------------------------------------------------------------------------------------------------
#-----------------------------------------------------------------------------------------------------------------------------------------------
#Create Flask route

@app.route('/')
def index():
    #Convert DataFrame to HTML
    table_html = df.to_html(classes='table table-striped', index=False)

    return render_template('index_stylized.html', table_html=table_html)

#-----------------------------------------------------------------------------------------------------------------------------------------------
#-----------------------------------------------------------------------------------------------------------------------------------------------
#Run the Flask app

if __name__ == '__main__':
    app.run(debug=True)